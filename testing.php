                                    <p class="card-text" id="post-subtitle-text">Alan subsequently met and married a young woman named Alyx Florin. He was unaware that she was actually Rose Canton (a.k.a. The Thorn), a schizophrenic but reformed enemy of the Flash. Alyx disappeared on their wedding night, starting a fire that led Alan to believe she had died. In fact, she survived and discovered that she was pregnant with twins. The children were born in Milwaukee, Wisconsin and raised separately by adoptive families as Jennifer-Lynn Hayden and Todd Rice. Alyx was taken to the Amazons' Reformation Island.</p>
