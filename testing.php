                                    <p class="card-text" id="post-subtitle-text">Alan was a founding member of the Justice Society and was instrumental in many wartime battles. After the war, as a broadcast executive in the late forties, Alan Scott had a flirtatious relationship with the costumed villainess the Harlequin (who unbeknownst to Alan, was secretly his secretary, Molly Mayne). Molly even admitted that her career as a "criminal" was just a ruse to get close to Green Lantern. The Harlequin eventually turned over a new leaf, becoming an undercover agent for the FBI. She left Alan's life without confessing her true identity or her real feelings.</p>
                                    
