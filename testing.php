                    <p class="card-text-subtitle-allinfo">Earth's original Green Lantern and a founding member of the Justice Society of America, Alan Scott has been a trailblazer for over 80 years.
                            Alan Scott is the first to bear the name Green Lantern, wielding a ring that focuses a mystical green flame. He was one of the first to put on a cape, becoming a founding member of the Justice Society of America. As time passed and Alan aged, he evolved into a mentor and inspiration to generations of new superheroes. Recharging his ring through his magic lantern, Green Lantern made his emerald glow a sign of hope and source of inspiration for decades.
                            Across the multiverse, on Earth-2, another Alan Scott wields the green flame after a tragic loss. This Green Lantern is the avatar of the Green, the elemental force that controls all living growth on Earth and focuses his powers through a ring meant for his dead partner. No less powerful or compelling than his earlier counterpart, he glows green to banish evil and help restore a war-torn world to its former greatness.</p>

                 
