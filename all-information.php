                                    <h2 class="card-title h4" id="post-title-text">Checkmate</h2>
                                    <p class="card-text" id="post-subtitle-text">Soon Alan was approached by the spy organization Checkmate, which was reorganizing under U.N. control. They invited Alan to become their White King, which he accepted. His time with Checkmate was brief.</p>
