                                    <h2 class="card-title h4" id="post-title-text">The Origin</h2>
                                    <p class="card-text" id="post-subtitle-text">Unlike others who bear the name "Green Lantern," Alan Scott's power does not come from the Guardians of the Universe, but from the Starheart, a magical entity once imprisoned by the Guardians. The Starheart found its way to Earth, where in 1939, in the shape of a lantern, it saved Alan from a train wreck (the "accident" was actually sabotage by Dekker, his rival). At the Lantern's mental direction, Alan fashioned a ring from it that allowed him to tap the Starheart's great power. Perhaps influenced subconsciously by the Starheart's history, Alan adopted the identity of Green Lantern. Alan Scott left ferroviary enginery and became employed in Apex Broadcasting Company, as a way to get any news about criminal activities in first hand. There he knew Doiby Dickles, a cab driver who became his sidekick, and Irene Miller, his first love interest.</p>
                                    
